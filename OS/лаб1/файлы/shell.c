//В данной строке происходит следующее :
//Когда вызывается fork(), операционная система создает новый процесс, который является точной копией родительского процесса, включая все его данные, код и состояние.
//В случае успеха, вызов fork() возвращает два разных значения разным процессам : ноль(0) в дочернем процессе и идентификатор нового процесса(PID) в родительском процессе.
// Это позволяет отличить код, выполняемый внутри родительского процесса от кода внутри дочернего процесса.
//Переменная child_pid объявлена с типом pid_t, который используется для хранения идентификатора процесса.
// Значение, возвращаемое fork(), присваивается child_pid, чтобы хранить идентификатор дочернего процесса внутри родительского процесса.
//Если вызов fork() завершился успешно, в родительском процессе переменная child_pid будет содержать идентификатор(PID) дочернего процесса, 
// что позволяет родительскому процессу контролировать и взаимодействовать с ним.
