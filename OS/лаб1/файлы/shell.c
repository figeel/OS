void proccess_input(char input[], char *tokens[], int token_count)

//Объявление функции proccess_input , которая принимает введенную строку, массив токенов и количество токенов. Эта функция будет использоваться для обработки ввода пользователя.


{                                          // Обрабатываем запрос от пользователя
    tokenize(input, tokens, &token_count); // Делим запрос на отдельные аргументы/токены
    //Вызов функции tokenize для разбиения введенной строки на отдельные токены (слова, разделенные пробелами)
    //и запись их в массив tokens. Количество токенов записывается в переменную token_count.


    update_input_output(tokens, token_count); // Проверяем запрос на наличие input/output file

    //Вызов функции update_input_output для обработки операторов перенаправления ввода и вывода в запросе пользователя

    if (token_count > 0)
        //Условный оператор, проверяющий наличие токенов. Если токенов нет, то нет смысла продолжать обработку.

    {
        int output_fd = -1;
        int oldoutput_fd = dup(STDOUT_FILENO); // Дубликация консольного вывода

        //Объявление переменных output_fd и oldoutput_fd. output_fd будет использоваться для хранения файлового дескриптора файла вывода. 
        // oldoutput_fd используется для сохранения стандартного файлового дескриптора вывода перед его изменением.
        // output_fd будет использован для открытия и перенаправления вывода в файл
        //oldoutput_fd позволит сохранить и восстановить стандартный вывод терминала после выполнения команды с перенаправлением вывода.
        //oldoutput_fd получает дубликат файлового дескриптора STDOUT_FILENO, чтобы сохранить текущий стандартный вывод перед его изменением



        if (output_file)
            //Условный оператор, проверяющий наличие файла вывода.
        {

            if (append_output)
            { // нашли оператор ">>" открываем на добавление текста
                output_fd = open(output_file, O_CREAT | O_RDWR | O_APPEND, 0644);
            }
            else
            {
                output_fd = open(output_file, O_CREAT | O_WRONLY | O_TRUNC, 0644);
            }

            //Если файл вывода существует, то в зависимости от флага append_output он открывается либо для добавления данных в конец файла, либо для записи данных в файл с заменой его содержимого.


            if (output_fd < 0)
            {
                fprintf(stderr, "Cannot open output file!\n");
                exit(EXIT_FAILURE);
            }
            //Если открытие файла не удалось (функция open вернула отрицательное значение), выводится сообщение об ошибке и программа завершается с кодом ошибки.



            if (dup2(output_fd, STDOUT_FILENO) == -1)
            {
                fprintf(stderr, "Cannot redirect output!\n");
            }

            //Функция dup2 используется для перенаправления стандартного вывода в файл. Если перенаправление не удалось, выводится сообщение об ошибке.
        }

        if (!execute_internal_command(tokens, token_count, output_file, append_output)) // Прошлись по внутренним, если не нашли идём во внешние

            //Вызывается функция execute_internal_command для выполнения внутренних команд оболочки. Если команда не является внутренней, функция вернет false,
            // и выполнение перейдет к следующему оператору.

        {
            execute_external_command(tokens, token_count, input_file, output_file, append_output);
            //Вызывается функция execute_external_command для выполнения внешних команд оболочки.

        }

        if (!!output_file) // Если был output file возвращаем output обратно терминалу
            //Условный оператор, проверяющий наличие файла вывода.

        {
            fflush(stdout);                    // Выводим всё что осталось в буффере
            dup2(oldoutput_fd, STDOUT_FILENO); // Возвращаем терминальный output обратно
            close(output_fd);                  // Закрываем файл
        }
        //Если файл вывода существует, то выполняется очистка буфера стандартного вывода, восстановление стандартного вывода и закрытие файла вывода.

    }
}


int main(int argc, char* argv[]) // Определяем главную функцию с аргументами argc (количество аргументов командной строки) и argv (массив строк аргументов)
{ // int main 
    if (argc == 2) // Проверяем, что были переданы два аргумента командной строки

    {
        FILE* cmd_source = fopen(argv[1], "r"); // Открываем файл для чтения
        //Открытие файла, имя которого передано вторым аргументом командной строки. Файл открывается для чтения.



        if (cmd_source == NULL) // Проверяем, удалось ли открыть файл

        {
            fprintf(stderr, "Cannot open commands file!\n"); // Печатаем сообщение об ошибке в поток ошибок

            exit(EXIT_FAILURE); // Завершаем программу с ошибкой

        }

        char buffer[MAX_INPUT_SIZE]; // Создаем буфер для чтения строки из файла
        //Создание буфера для чтения строки из файла. Размер буфера определен константой MAX_INPUT_SIZE.


        char* buf_tokens[MAX_TOKENS]; // Создаем массив для хранения токенов
        //Создание массива для хранения токенов(отдельных частей введенной строки).

        int buf_token_count = 0; // Инициализируем счетчик токенов значением 0
        //

        while (fgets(buffer, sizeof(buffer), cmd_source) != NULL) // Считываем строки из файла, пока файл не закончится

        {
            proccess_input(buffer, buf_tokens, buf_token_count); // Обрабатываем каждую строку
            //Обработка каждой прочитанной строки функцией proccess_input.

        }

        fclose(cmd_source); // Закрываем файл


        return EXIT_SUCCESS; // Завершаем программу с кодом успешного выполнения

    }

    // Получаем текущую рабочую папку

    char* cwd = getcwd(NULL, 0); // Получаем текущую рабочую папку

    if (cwd == NULL) // Проверяем, удалось ли получить текущую рабочую папку
        //Проверка, удалось ли получить текущую рабочую директорию.

    {
        fprintf(stderr, "Cannot get current directory!\n"); // Печатаем сообщение об ошибке в поток ошибок

        exit(EXIT_FAILURE); // Завершаем программу с кодом ошибкой

    }

    // Выделяем память для строки с путем до файла оболочки

    char* shell_path = (char*)malloc(strlen(cwd) + strlen(SHELL_NAME) + 2); // Выделяем память под строку для пути к файлу оболочки
    //Выделяем память под строку для пути к файлу оболочки. Размер выделяемой памяти равен сумме длин текущей директории, имени оболочки и двух символов (для слэша и нуль-терминатора).



    if (shell_path == NULL) // Проверяем, удалось ли выделить память

    {
        fprintf(stderr, "malloc failed!\n"); // Печатаем сообщение об ошибке в поток ошибок
        //Если память не выделилась, выводим сообщение об ошибке в поток ошибок.


        free(cwd); // Освобождаем память, выделенную под текущую рабочую папку
        //Освобождаем память, ранее выделенную под строку с текущей рабочей директорией.


        exit(EXIT_FAILURE); // Завершаем программу с ошибкой

    }

    // Формируем путь к файлу оболочки и выставляем переменную окружения

    sprintf(shell_path, "%s/%s", cwd, SHELL_NAME); // Формируем путь к файлу оболочки
    //Формируем строку с путем к файлу оболочки. В получившуюся строку включается путь к текущей директории, слэш и имя оболочки.


    setenv("SHELL", shell_path, 1); // Устанавливаем переменную окружения "SHELL" с путем к файлу оболочки
    //Устанавливаем переменную окружения "SHELL" значением пути к файлу оболочки.

    free(cwd); // Освобождаем память, выделенную под текущую рабочую папку
    //Освобождаем память, ранее выделенную под строку с текущей рабочей директорией
    free(shell_path); // Освобождаем память, выделенную под путь к файлу оболочки
    //Освобождаем память, ранее выделенную под строку с путем к файлу оболочки.

    char input[MAX_INPUT_SIZE]; // Создаем буфер для хранения входной строки
    //Создаем буфер для хранения входной строки. Размер буфера определен константой MAX_INPUT_SIZE.
    char* tokens[MAX_TOKENS]; // Создаем массив для хранения токенов
    //Создаем массив для хранения токенов (отдельных частей введенной строки).

    int token_count = 0; // Инициализируем счетчик токенов
    //Инициализируем счетчик токенов значением 0.

    while (true) // Бесконечный цикл


    {
        char* current_dir = getcwd(NULL, 0); // Получаем текущую рабочую папку

        printf("%s$ ", current_dir); // Выводим приглашение, содержащее путь к текущей папке
        //Выводим приглашение к вводу команд, которое содержит путь к текущей рабочей директории и знак доллара.

        free(current_dir); // Освобождаем память, выделенную под текущую рабочую папку


        // Если не можем получить входную строку или достигнут конец файла

        if (fgets(input, MAX_INPUT_SIZE, stdin) == NULL) // Считываем входную строку
            //Считываем входную строку. Если строка не считалась (достигнут конец файла), то условие выполнится.

        {
            break; // Выходим из цикла

        }

        proccess_input(input, tokens, token_count); // Обрабатываем входную строку функцией proccess_input.

    }

    return EXIT_SUCCESS; // Завершаем программу с кодом успешного выполнения

}


