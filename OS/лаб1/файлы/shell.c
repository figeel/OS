void execute_external_command(char* tokens[], int token_count, char* input_file, char* output_file, bool append_output)
{ // execute_external_command char* tokens[] - массив токенов, int token_count - кол-во элементов в массиве, 
    // char* input_file - Путь к файлу для перенаправления входа(вывода), bool append_output - нужно ли добавлять в выходной файл (>>) или перезаписывать (>)
    bool background = false; // Создаем переменную-флаг для определения, будет ли команда выполняться в фоновом режиме

    // Проверяем, является ли последний токен символом "&". Если да, то команда должна выполняться в фоновом режиме.
    if (token_count > 0 && strcmp(tokens[token_count - 1], "&") == 0)
    {
        background = true; // Устанавливаем флаг в значение true
        tokens[token_count - 1] = NULL; // Заменяем последний токен на NULL
    }

    // Создаем дочерний процесс с помощью системного вызова fork()
    pid_t child_pid = fork();//

    if (child_pid == 0) // Если мы находимся в дочернем процессе
    {
        if (input_file) // Проверяем, задан ли входной файл для команды
        {
            int input_fd = open(input_file, O_RDONLY); // Открываем входной файл только для чтения
            int oldinput_fd = dup(0); // Делаем дубликат дескриптора файла stdin

            if (input_fd < 0) // Проверяем, успешно ли открылся файл
            {
                fprintf(stderr, "Cannot open input file!\n"); // Выводим сообщение об ошибке
                exit(EXIT_FAILURE); // Завершаем работу программы с кодом ошибки
            }

            // Перенаправляем стандартный ввод на входной файл
            // В этой строке кода используется системный вызов dup2() для перенаправления стандартного ввода (stdin) на файловый дескриптор input_fd. 
            //Если перенаправление завершается неудачно (возвращает -1), то условие if оценивается как истинное, и код внутри блока if выполняется.

            if (dup2(input_fd, STDIN_FILENO) == -1)
            {
                fprintf(stderr, "Cannot redirect input!\n"); // Выводим сообщение об ошибке
            }

            close(input_fd); // Закрываем дескриптор входного файла
        }

        if (background)
        { // Если флаг background равен true, то команда выполняется в фоновом режиме
            int dev_null_fd = open("/dev/null", O_RDWR); // Открываем файл /dev/null для чтения и записи

            if (dev_null_fd < 0) // Проверяем, успешно ли открылся файл
            {
                fprintf(stderr, "Cannot access /dev/null!\n"); // Выводим сообщение об ошибке
                exit(EXIT_FAILURE); // Завершаем работу программы с кодом ошибки
            }

            if (!input_file) // Если входной файл не задан
            {
                dup2(dev_null_fd, STDIN_FILENO); // Перенаправляем стандартный ввод на файл /dev/null
            }

            if (!output_file) // Если выходной файл не задан
            {
                dup2(dev_null_fd, STDOUT_FILENO); // Перенаправляем стандартный вывод на файл /dev/null
            }
            if (strcmp(tokens[0], "top") == 0) { // Если первый токен равен "top"
                tokens[token_count - 1] = "-n"; // Заменяем последний токен на "-n"
                tokens[token_count] = "1"; // Добавляем токен "1"
                tokens[token_count + 1] = "-b"; // Добавляем токен "-b"
                tokens[token_count + 2] = NULL; // Добавляем NULL-токен
            }
            execvp(tokens[0], tokens); // Запускаем внешнюю команду
            dup2(dev_null_fd, STDERR_FILENO); // Перенаправляем стандартный вывод ошибок на файл /dev/null

            close(dev_null_fd); // Закрываем дескриптор файла /dev/null
        }
        else // Если флаг background равен false, то команда выполняется в обычном режиме
        {
            execvp(tokens[0], tokens); // Запускаем внешнюю команду
            // tokens является массивом аргументов команды, где tokens[0] представляет имя команды, а последующие элементы tokens представляют аргументы команды. Функция execvp()
            // ищет исполняемый файл с именем tokens[0]
            //в путях поиска операционной системы и заменяет текущий процесс на исполняемый файл команды, передавая ему массив аргументов tokens.
            fprintf(stderr, "Cannot exec program!\n"); // Выводим сообщение об ошибке, если команда не может быть выполнена
            exit(EXIT_FAILURE); // Завершаем работу программы с кодом ошибки
        }
    }
    else if (child_pid > 0) // Если мы находимся в родительском процессе
    {
        if (!background) // Если задача не фоновая
        {
            waitpid(child_pid, NULL, 0); // Ждем завершения дочернего процесса
        }
    }
    else // Если процесс не может быть создан
    {
        fprintf(stderr, "Cannot fork process!\n"); // Выводим сообщение об ошибке
    }
}


