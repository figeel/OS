#include "queue.h"  // Подключение заголовочного файла, содержащего определения структур и прототипы функций

// Функция для инициализации очереди
void initialize_queue(queue_t* queue) {
    queue->size = 0;          // Устанавливаем размер очереди в 0
    queue->head = NULL;       // Начало очереди (голова) указывает на NULL — очередь пуста
    queue->tail = NULL;       // Конец очереди (хвост) тоже NULL — очередь пуста
}

// Функция для добавления элемента в конец очереди
int enqueue(queue_t* queue, void* data) {
    node_t* new_node = (node_t*)malloc(sizeof(node_t));  // Выделяем память под новый узел очереди
    if (!new_node) {                                     // Проверка, удалось ли выделить память
        return 1;                                        // Если нет — возвращаем код ошибки
    }
    new_node->data = data;        // Сохраняем переданный указатель на данные в узле
    new_node->next = NULL;        // Следующего элемента пока нет — NULL

    // Если очередь пуста, устанавливаем новый узел как начало и конец
    if (queue->tail == NULL) {
        queue->head = new_node;   // Начало очереди указывает на новый узел
        queue->tail = new_node;   // Конец очереди тоже указывает на него
    }
    else {
        queue->tail->next = new_node; // Предыдущий последний узел теперь указывает на новый
        queue->tail = new_node;       // Новый узел становится последним в очереди
    }
    queue->size++;  // Увеличиваем размер очереди

    return 0;  // Возвращаем 0 — успешное добавление
}

// Функция для удаления элемента из начала очереди
void* dequeue(queue_t* queue) {
    if (queue->head == NULL) {      // Проверка: если очередь пуста
        return NULL;                // Вернуть NULL — удалять нечего
    }

    void* data = queue->head->data; // Сохраняем данные из первого узла
    node_t* temp = queue->head; // Сохраняем указатель на удаляемый узел
    queue->head = queue->head->next; // Перемещаем голову на следующий узел

    if (queue->head == NULL) {       // Если после удаления очередь стала пустой
        queue->tail = NULL;          // Обнуляем и хвост
    }

    free(temp);       // Освобождаем память, занятую удалённым узлом
    queue->size--;    // Уменьшаем размер очереди

    return data;      // Возвращаем данные, которые были в удалённом узле
}

// Функция для проверки, пуста ли очередь
int is_empty(queue_t* queue) {
    return queue->head == NULL;  // Если голова NULL — очередь пуста, возвращаем 1 (истина)
}
