#pragma once                // Гарантирует, что заголовочный файл будет включён только один раз при компиляции

#ifndef QUEUE_H             // Начало условной компиляции: если QUEUE_H не определён
#define QUEUE_H             // Определяем QUEUE_H, чтобы избежать повторного включения

#include <stdio.h>          // Подключение стандартной библиотеки ввода/вывода (на случай отладки, например printf)
#include <stdlib.h>         // Для использования malloc, free и других функций управления памятью
#include <stddef.h>         // Для определения типа size_t (может быть полезен при работе с размерами данных)

// Структура для представления одного элемента очереди (узла)
typedef struct node_s {
    void* data;             // Указатель на произвольные данные (обеспечивает универсальность очереди)
    struct node_s* next;    // Указатель на следующий элемент очереди
} node_t;

// Структура для представления всей очереди
typedef struct {
    node_t* head;           // Указатель на начало очереди (первый элемент)
    node_t* tail;           // Указатель на конец очереди (последний элемент)
    int size;               // Текущее количество элементов в очереди
} queue_t;

// Прототип функции инициализации очереди
void initialize_queue(queue_t* queue);

// Прототип функции добавления элемента в конец очереди
int enqueue(queue_t* queue, void* data);

// Прототип функции удаления элемента из начала очереди
void* dequeue(queue_t* queue);

// Прототип функции проверки, пуста ли очередь
int is_empty(queue_t* queue);

#endif   // Завершение условной компиляции
